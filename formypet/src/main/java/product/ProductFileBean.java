package product;

public class ProductFileBean {
	
	private int fileKey;
	private String fileOriginalName;
	private String fileSaveName;
	private int size;
	private String delYn;
	private String fileCreatedDate;
	private String fileDeletedDate;
	private int productKey;
	
	public int getFileKey() {
		return fileKey;
	}
	public void setFileKey(int fileKey) {
		this.fileKey = fileKey;
	}
	public String getFileOriginalName() {
		return fileOriginalName;
	}
	public void setFileOriginalName(String fileOriginalName) {
		this.fileOriginalName = fileOriginalName;
	}
	public String getFileSaveName() {
		return fileSaveName;
	}
	public void setFileSaveName(String fileSaveName) {
		this.fileSaveName = fileSaveName;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public String getDelYn() {
		return delYn;
	}
	public void setDelYn(String delYn) {
		this.delYn = delYn;
	}
	public String getFileCreatedDate() {
		return fileCreatedDate;
	}
	public void setFileCreatedDate(String fileCreatedDate) {
		this.fileCreatedDate = fileCreatedDate;
	}
	public String getFileDeletedDate() {
		return fileDeletedDate;
	}
	public void setFileDeletedDate(String fileDeletedDate) {
		this.fileDeletedDate = fileDeletedDate;
	}
	public int getProductKey() {
		return productKey;
	}
	public void setProductKey(int productKey) {
		this.productKey = productKey;
	}
	
}
